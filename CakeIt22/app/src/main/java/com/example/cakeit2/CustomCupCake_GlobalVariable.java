package com.example.cakeit2;

public class CustomCupCake_GlobalVariable {

    String sponge, filling, frosting, toppings, candles, quantity, name, price, orderid, orderstatus, username, usercontact, uid, timeorder, timereceived;

    public CustomCupCake_GlobalVariable(String sponge, String filling, String frosting, String toppings, String candles, String quantity, String name, String price, String orderid, String orderstatus, String username, String usercontact, String uid, String timeorder, String timereceived) {
        this.sponge = sponge;
        this.filling = filling;
        this.frosting = frosting;
        this.toppings = toppings;
        this.candles = candles;
        this.quantity = quantity;
        this.name = name;
        this.price = price;
        this.orderid = orderid;
        this.orderstatus = orderstatus;
        this.username = username;
        this.usercontact = usercontact;
        this.uid = uid;
        this.timeorder = timeorder;
        this.timereceived = timereceived;
    }

    public CustomCupCake_GlobalVariable() {
    }

    public String getSponge() {
        return sponge;
    }

    public String getFilling() {
        return filling;
    }

    public String getFrosting() {
        return frosting;
    }

    public String getToppings() {
        return toppings;
    }

    public String getCandles() {
        return candles;
    }

    public String getQuantity() {
        return quantity;
    }

    public String getName() {
        return name;
    }

    public String getPrice() {
        return price;
    }

    public String getOrderid() {
        return orderid;
    }

    public String getOrderstatus() {
        return orderstatus;
    }

    public String getUsername() {
        return username;
    }

    public String getUsercontact() {
        return usercontact;
    }

    public String getUid() {
        return uid;
    }

    public String getTimeorder() {
        return timeorder;
    }

    public String getTimereceived() {
        return timereceived;
    }
}


