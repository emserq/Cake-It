        this.sponge = sponge;
        this.filling = filling;
        this.frosting = frosting;
        this.topping = topping;
        this.candle = candle;
        this.quantity = quantity;
        this.name = name;
        this.price = price;
        this.orderid = orderid;
        this.orderstatus = orderstatus;
        this.username = username;
        this.usercontact = usercontact;
